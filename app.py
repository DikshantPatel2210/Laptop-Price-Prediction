import streamlit as st
import joblib
import numpy as np
pipe = joblib.load("df.joblib")
df = joblib.load("pipe.joblib")
st.title("Laptop Predictor")
company = st.selectbox("Brand", df["Company"].unique())
type1 = st.selectbox("Type", df["TypeName"].unique())
ram = st.selectbox("RAM(in GB)", [2,4,6,8,12,16,24,32,64])
weight = st.number_input('Enter your weight')
touchscreen = st.selectbox("Touchscreen", ["No","Yes"])
ips = st.selectbox("IPS",["No", "Yes"])
screen_size = st.number_input("Screen Size")
resolution = st.selectbox("Screen Resolution", ["1920x1080", "1366x768", "1600x900", "3840x2160", "3200x1800","2880x1800","2560x1600","2560x1440","2304x1440"])
CPU = st.selectbox("CPU" , df["Cpu brand"].unique())
HDD = st.selectbox("HDD(in GB)" , [8,128,256,512,1024,2048])
SSD = st.selectbox("SSD(in GB)" , [0,8,128,256,512,1024])
GPU = st.selectbox("GPU" , df["Gpu brand"].unique())
OS = st.selectbox("os", df["os"].unique())

if st.button("Predict Price"):
  ppi = None
  if touchscreen == "Yes":
    touchscreen = 1
  else :
    touchscreen = 0

  if ips == "Yes":
    ips = 1
  else :
    ips = 0

  X_res = int(resolution.split("x")[0])
  Y_res = int(resolution.split("x")[1])
  ppi = ((X_res**2) + (Y_res**2))**0.5/screen_size
  query = np.array([company, type1, ram, weight, touchscreen, ips, ppi, CPU, HDD, SSD, GPU,OS])
  query = query.reshape(1,12)
  st.title("The Predicted Price Of This Configuration is : " , +str(int(np.exp(pipe.predict(query)[0]))))